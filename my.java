
class my
{

    public static int adds( int a ,  int b ,  int c){
        return  a + b + c;
    }


    public static int subs( int a ,  int b ,  int c){
        return  a - b - c;
    }


    public static int muls( int a ,  int b ,  int c){
        return  a * b * c;
    }


    public static int divs( int a){
        return  a / 2;
    }

}